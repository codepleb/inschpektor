<template>
  <div v-if="neighbors">
    <article class="message" :class="[issues(neighbors).length === 0 ? 'is-success' : 'is-warning']" v-on:click="showContent = !showContent">
      <div class="message-header">
        <p>Summary - Neighbors: {{neighbors.length}}</p>

        <a v-if="showContent" class="button is-warning" :key="1">
          <i class="fas fa-window-minimize"></i>
        </a>

        <a v-else class="button is-warning" :key="0">
          <i class="far fa-window-maximize"></i>
        </a>

      </div>
      <div class="message-body" v-if="showContent">
        <p v-if="issues(neighbors).length === 0">Everything is fine! :)</p>
        <p v-for="issue in issues(neighbors)">â€¢ {{issue}}</p>
      </div>
    </article>
  </div>
</template>

<script>
  export default {
    name: 'Summary',
    props: ['neighbors'],
    data() {
      return {
        showContent: true
      };
    },
    methods: {
      issues: (neighbors) => {
        const issueMsgs = [];

        const addIssueMessage = (neighbor, msg) => {
          issueMsgs.push(`Neighbor ${neighbor.address} ${msg}`);
        };
        neighbors.forEach(n => {
          if (!n.isFriendlyNode) addIssueMessage(n, 'has a lot of invalid transactions.');
          if (n.isSynced === false) addIssueMessage(n, 'does not seem to be in sync.');
          if (n.isActive === false) addIssueMessage(n, 'does not seem to be active anymore.');
        });

        return issueMsgs;
      }
    },
  };
</script>

<style scoped>
  div {
    padding-bottom: 10px;
  }
</style>